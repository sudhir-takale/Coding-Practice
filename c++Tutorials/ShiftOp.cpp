#include<bits/stdc++.h>
using namespace std;

int main() {

    // left shift in bits  
    // Multiply number by 2 
    
    int a = 10;
     int c = a << 2;  // 10 *2 = 20 , 20 * 2 = 40
    cout << c << endl;
    
    // right shift in bits

    int d = 10;
    int k = d >> 2 ;  // 10/ 2 = 5 , 5 / 2 = 2 
    cout << k << endl;


    return 0;
}